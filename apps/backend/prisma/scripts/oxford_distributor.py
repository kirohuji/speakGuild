# 1. Parse Oxford 3000
# Format per line: "word part_of_speech CEFR" or "word part_of_speech | CEFR, n. CEFR"
raw = open(OXFORD_FILE, encoding="utf-8").read()

entries = []  # (word, pos, cefr)
for line in raw.split("\n"):
    line = line.strip()
    # Skip header/footer lines
    if not line or line.startswith("©") or line.startswith("The Oxford"):
        continue
    # Parse: "abandon v. B2" or "ability n. A2" or "about prep., adv. A1"
    # Pattern: word [pos] CEFR
    m = re.match(r"^([a-z\s\-()\']+?)\s+((?:[a-z]+\.,?\s*)+)\s+([A-B]\d)", line)
    if m:
        word = m.group(1).strip()
        pos_raw = m.group(2).strip()
        cefr = m.group(3).strip()
        # Extract first POS
        pos = pos_raw.split(".")[0].split(",")[0].strip()
        if word and pos and cefr:
            entries.append((word, pos, cefr))

print(f"Parsed {len(entries)} Oxford 3000 entries")

# Stats by CEFR
from collections import Counter

cefr_counts = Counter(e[2] for e in entries)
print(f"By CEFR: {dict(cefr_counts)}")

# Get unique words
all_oxford_words = set(e[0].lower() for e in entries)
