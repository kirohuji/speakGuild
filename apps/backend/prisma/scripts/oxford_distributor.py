pos_map = {
    "v.": "verb",
    "n.": "noun",
    "adj.": "adj",
    "adv.": "adv",
    "prep.": "prep",
    "conj.": "conj",
    "pron.": "pron",
    "det.": "det",
    "num.": "num",
    "intj.": "intj",
    "modal": "modal",
    "aux": "aux",
    "phrasal": "phrasal",
}


def norm_pos(p):
    p = p.strip().lower()
    if p in pos_map:
        return pos_map[p]
    if p.startswith("v"):
        return "verb"
    if p.startswith("n"):
        return "noun"
    if p.startswith("adj"):
        return "adj"
    if p.startswith("adv"):
        return "adv"
    if p.startswith("prep"):
        return "prep"
    if p.startswith("conj"):
        return "conj"
    if p.startswith("pron"):
        return "pron"
    if p.startswith("det"):
        return "det"
    if p.startswith("num"):
        return "num"
    if p.startswith("int"):
        return "intj"
    if p.startswith("modal"):
        return "modal"
    if p.startswith("aux"):
        return "aux"
    return p.split("/")[0].strip()


def match_theme(word, themes):
    """Check if word matches any theme keyword"""
    wl = word.lower()
    for theme_words in themes:
        for kw in theme_words:
            if kw in wl or wl in kw:
                return True
    return False


